def main():
    recommender = MovieRecommender()  # Create an instance of MovieRecommender

    while True:
        print("Movie Recommender System")
        print("1. Rate a Movie")
        print("2. Get Recommendations")
        print("3. Exit")
        choice = input("Enter your choice (1/2/3): ")

        if choice == '1':
            title = input("Enter the movie title: ")
            genre = input("Enter the movie genre: ")
            rating = int(input("Enter your rating (1-5): "))
            recommender.rate_movie(title, genre, rating)  # Rate the movie provided by the user

        elif choice == '2':
            print("Select a genre:")
            for num, genre in recommender.genres.items():
                print(f"{num}. {genre}")
            genre_choice = int(input("Enter the number of your choice: "))
            genre = recommender.genres.get(genre_choice, None)  # Get the genre based on user choice
            if genre:
                rating = int(input("Enter the rating (1-5) to filter by: "))
                recommendations = recommender.get_recommendations(genre, rating)  # Get movie recommendations
                if recommendations:
                    print("Recommended Movies:")
                    for movie in recommendations:
                        print(f"- {movie}")
                else:
                    print("No movies found with the specified genre and rating.")
            else:
                print("Invalid genre choice.")

        elif choice == '3':
            print("Exiting the Movie Recommender System. Goodbye!")
            sys.exit()  # Exit the program

        else:
            print("Invalid choice. Please enter 1, 2, or 3.")


